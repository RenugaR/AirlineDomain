package org.banks;

public class HdfcBank {
	public static void main(String[] args) {

		// dataType varaibleName=value;
		// byte
		byte b = 127;
		System.out.println("Byte value of b is " + b);

		// short
		short s = 32000;
		System.out.println("Short value is " + s);

		// int
		int a = 98765432;
		System.out.println("Integer value is " + a);

		// long
		long l = 987654323l;
		System.out.println("Long value is " + l);
		
		//double
		double d=5469.098;
		System.out.println("Double value is "+d);
		
		//float
		float f=987.34f;
		System.out.println("Float value is "+f);
		
		//boolean
		boolean b1=true;
		System.out.println("Boolean value is "+b1);
		
		//char-->it will store only one char
		char c='A';
		char c1='@';
		char c2='2';
		System.out.println("Char value is "+ c + " " +c1 +" " +c2);
		//String-->combination of all char or words
		String s1="Hello";
		String s2="34567890";
		String s3="@#$%^&*()";
		System.out.println("String value is "+s1);
		System.out.println("String value is "+s2);
		System.out.println("String value is "+s3);
	}
}
